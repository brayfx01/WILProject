public class padding {
    public static void main(String[] args) {
        int paddingSize = 5; // Change this size as needed

        // Create a string with the desired number of empty spaces using String.repeat
        String padding = " ".repeat(paddingSize);
        
        System.out.println("[" + padding + "]"); // Enclosing the padding in square brackets for visibility
        
    }

}
